// Performance level helpers
export const getPerformanceLevel = (
  pointUsage: number
): "High" | "Medium" | "Low" => {
  if (pointUsage >= 80) return "High";
  if (pointUsage >= 50) return "Medium";
  return "Low";
};

export const getPerformanceColor = (pointUsage: number): string => {
  if (pointUsage >= 80) return "text-green-600";
  if (pointUsage >= 50) return "text-yellow-600";
  return "text-red-600";
};

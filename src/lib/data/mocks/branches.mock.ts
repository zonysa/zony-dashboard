// Helper functions for filtering
export const getBranchesByCity = (city: string) => {
  return mockBranches.filter((branch) => branch.city === city);
};

export const getBranchesByStatus = (status: string) => {
  return mockBranches.filter((branch) => branch.status === status);
};

export const getBranchesByPerformance = (minUsage: number) => {
  return mockBranches.filter((branch) => branch.pointUsage >= minUsage);
};

